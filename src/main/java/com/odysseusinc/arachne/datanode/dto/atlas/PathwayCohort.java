package com.odysseusinc.arachne.datanode.dto.atlas;

public class PathwayCohort {
    private Integer id;
    private String name;
    private String description;
    private String code;
    private Integer pathwayCohortId;

    public Integer getId() {

        return id;
    }

    public void setId(Integer id) {

        this.id = id;
    }

    public String getName() {

        return name;
    }

    public void setName(String name) {

        this.name = name;
    }

    public String getDescription() {

        return description;
    }

    public void setDescription(String description) {

        this.description = description;
    }

    public String getCode() {

        return code;
    }

    public void setCode(String code) {

        this.code = code;
    }

    public Integer getPathwayCohortId() {

        return pathwayCohortId;
    }

    public void setPathwayCohortId(Integer pathwayCohortId) {

        this.pathwayCohortId = pathwayCohortId;
    }
}
